package fyp.tingli.model;

public class F3Airline {
	
	private String airport_iata;
	private String airport_icao;
	private String short_name;
	private String host_airport_iata;
	private String d_or_i;
	private String name_xml;
	private String address;
	private String contact_person;
	private String phone;
	private String remark;
	
	public String getAirport_iata() {
		return airport_iata;
	}
	public void setAirport_iata(String airport_iata) {
		this.airport_iata = airport_iata;
	}
	public String getAirport_icao() {
		return airport_icao;
	}
	public void setAirport_icao(String airport_icao) {
		this.airport_icao = airport_icao;
	}
	public String getShort_name() {
		return short_name;
	}
	public void setShort_name(String short_name) {
		this.short_name = short_name;
	}
	public String getHost_airport_iata() {
		return host_airport_iata;
	}
	public void setHost_airport_iata(String host_airport_iata) {
		this.host_airport_iata = host_airport_iata;
	}
	public String getD_or_i() {
		return d_or_i;
	}
	public void setD_or_i(String d_or_i) {
		this.d_or_i = d_or_i;
	}
	public String getName_xml() {
		return name_xml;
	}
	public void setName_xml(String name_xml) {
		this.name_xml = name_xml;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContact_person() {
		return contact_person;
	}
	public void setContact_person(String contact_person) {
		this.contact_person = contact_person;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	

}
