package fyp.tingli.model;

public class F3Remark {

	private String remark_code;
	private String name_xml;
	private String description;
	
	public String getRemark_code() {
		return remark_code;
	}
	public void setRemark_code(String remark_code) {
		this.remark_code = remark_code;
	}
	public String getName_xml() {
		return name_xml;
	}
	public void setName_xml(String name_xml) {
		this.name_xml = name_xml;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
