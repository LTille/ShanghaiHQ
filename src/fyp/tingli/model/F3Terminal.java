package fyp.tingli.model;

public class F3Terminal {
	
	private String terminal_code;
	private String airport_iata;
	private String name_xml;
	private String description;
	
	public String getTerminal_code() {
		return terminal_code;
	}
	public void setTerminal_code(String terminal_code) {
		this.terminal_code = terminal_code;
	}
	public String getAirport_iata() {
		return airport_iata;
	}
	public void setAirport_iata(String airport_iata) {
		this.airport_iata = airport_iata;
	}
	public String getName_xml() {
		return name_xml;
	}
	public void setName_xml(String name_xml) {
		this.name_xml = name_xml;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}
